async function main(): Promise<void> {
  printPrerequisites();

  const suiteResults = await readJson<LoopResults>(SUITE_RESULTS_PATH);
  if (!suiteResults) {
    console.error(`Missing ${path.relative(REPO_ROOT, SUITE_RESULTS_PATH)} — run npm run test:suite first.`);
    process.exitCode = 1;
    return;
  }

  const missingDescriptions = generateTestCases().filter((c) => !c.description);
  if (missingDescriptions.length > 0) {
    console.warn(
      `Warning: ${missingDescriptions.length} case(s) in generator.ts have no description and will be flagged in TEST-SCORES.md: ${missingDescriptions.map((c) => c.name).join(", ")}`,
    );
  }

  await writeFile(TEST_SCORES_PATH, await buildTestScoresMd(suiteResults), "utf-8");
  console.log(`Wrote ${path.relative(REPO_ROOT, TEST_SCORES_PATH)}`);

  const benchmarks: Array<{ id: string; label: string; payload: BenchmarkResults }> = [];
  for (const lib of BENCHMARK_LIBRARIES) {
    const resultsPath = path.join(BENCHMARK_OUTPUT, `results-${lib.id}.json`);
    const payload = await readJson<BenchmarkResults>(resultsPath);
    if (payload) {
      benchmarks.push({ id: lib.id, label: payload.library.npm, payload });
    } else {
      console.warn(
        `Warning: no benchmark results for "${lib.id}" (${path.relative(REPO_ROOT, resultsPath)} not found) — it will be missing from BENCHMARK.md. Run npm run test:benchmark.`,
      );
    }
  }

  if (benchmarks.length === 0) {
    console.error(
      `No benchmark results found under ${path.relative(REPO_ROOT, BENCHMARK_OUTPUT)} — skipping BENCHMARK.md. Run npm run test:benchmark first.`,
    );
    process.exitCode = 1;
    return;
  }

  const staleWarnings = checkStaleness(suiteResults, benchmarks);
  if (staleWarnings.length > 0) {
    console.warn("\nStaleness warnings:");
    for (const w of staleWarnings) console.warn(`  - ${w}`);
    console.warn("");
  }

  await writeFile(BENCHMARK_PATH, await buildBenchmarkMd(suiteResults, benchmarks), "utf-8");
  console.log(`Wrote ${path.relative(REPO_ROOT, BENCHMARK_PATH)}`);
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});
