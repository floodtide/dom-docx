function caseTable(cases: CaseResult[], descriptions: Map<string, string>): string {
  const rows = cases.map((c) => {
    const desc = descriptions.get(c.name);
    const flaggedName = desc ? c.name : `${c.name} ⚠️ no description in generator.ts`;
    const xml = c.error ? "ERR" : c.xmlPassed ? "✓" : "✗";
    const misaligned = c.mismatchedPixels !== undefined ? c.mismatchedPixels.toLocaleString() : "—";
    return `| \`${flaggedName}\` | ${desc ?? "—"} | ${xml} | ${fmtPct(c.visualScore)} | ${misaligned} |`;
  });
  return [
    "| Test | Description | XML | Visual | Misaligned px |",
    "|------|-------------|-----|--------|---------------|",
    ...rows,
  ].join("\n");
}

async function buildTestScoresMd(suiteResults: LoopResults): Promise<string> {
  const standardNames = new Set(generateStandardTestCases().map((c) => c.name));
  const descriptions = new Map(generateTestCases().map((c) => [c.name, c.description ?? ""]));

  const standardCases = suiteResults.cases.filter((c) => standardNames.has(c.name));
  const edgeCases = suiteResults.cases.filter((c) => !standardNames.has(c.name));
  const allRollup = suiteResults.suite;
  const standardRollup = rollup(standardCases);
  const edgeRollup = rollup(edgeCases);

  const calibration = await readJson<CalibrationResults>(CALIBRATION_PATH);
  const calibrationRow = calibration
    ? `| Identity-pair calibration (full ${calibration.cases.length}) | — | — | **mean ${fmtPct(calibration.meanAdjustedCeiling)} / min ${fmtPct(calibration.minAdjustedCeiling)}** |`
    : null;

  const humanLabels = await readJson<{ labels: Record<string, { rating?: number; note?: string }> }>(
    HUMAN_LABELS_PATH,
  );

  const lowest = [...suiteResults.cases]
    .filter((c) => c.visualScore !== null)
    .sort((a, b) => (a.visualScore ?? 0) - (b.visualScore ?? 0))
    .slice(0, 8);

  const hasHumanLabels = humanLabels && lowest.some((c) => humanLabels.labels[c.name]?.rating);
  const lowestHeader = hasHumanLabels
    ? "| Test | Visual | Human | Notes |\n|------|-------:|:-----:|-------|"
    : "| Test | Visual | Notes |\n|------|-------:|-------|";
  const lowestRows = lowest.map((c) => {
    const desc = descriptions.get(c.name) ?? "—";
    if (hasHumanLabels) {
      const label = humanLabels!.labels[c.name];
      return `| \`${c.name}\` | ${fmtPct(c.visualScore)} | ${label?.rating ?? "—"} | ${label?.note ?? desc} |`;
    }
    return `| \`${c.name}\` | ${fmtPct(c.visualScore)} | ${desc} |`;
  });

  return `# Validation scores

${GENERATED_NOTICE}

To generate suite metrics, run \`npm run test:suite\` then \`npm run docs:sync\`. **How scores are computed:** [SCORING.md](./SCORING.md).

Last regenerated from a suite run at **${suiteResults.runAt}** (mode: ${suiteResults.mode}).

## Summary

| Metric | Standard (${standardCases.length}) | Edge (${edgeCases.length}) | All (${suiteResults.cases.length}) |
|--------|---------------|-----------|----------|
| XML schema pass | ${standardRollup.xmlPassCount} / ${standardCases.length} | ${edgeRollup.xmlPassCount} / ${edgeCases.length} | **${allRollup.xmlPassCount} / ${suiteResults.cases.length}** |
| Avg **visual (layout-based)** | ${fmtPct(standardRollup.visual)} | ${fmtPct(edgeRollup.visual)} | **${fmtPct(allRollup.visual)}** |
| Avg raw layout (pre-guards) | ${fmtPct(standardRollup.layout)} | ${fmtPct(edgeRollup.layout)} | **${fmtPct(allRollup.layout)}** |
| Avg pixel match (tripwire, unscored) | ${fmtPct(standardRollup.pixelMatch)} | ${fmtPct(edgeRollup.pixelMatch)} | **${fmtPct(allRollup.pixelMatch)}** |
| Avg engine score | ${fmtScore(standardRollup.engine)} | ${fmtScore(edgeRollup.engine)} | **${fmtScore(allRollup.engine)}** |
| Avg compile | — | — | **${fmtMs(allRollup.avgPerformanceMs)}** |
${calibrationRow ?? "| Identity-pair calibration | — | — | _run `npm run test:calibration` for this row_ |"}

Tables below use the **layout-based visual** score; misaligned px is the raw pixel tripwire.

---

## Standard baseline (${standardCases.length})

${caseTable(standardCases, descriptions)}

## Edge cases (${edgeCases.length})

${caseTable(edgeCases, descriptions)}

---

## Lowest scores (current priorities)

Bottom ${lowest.length} cases by layout-based visual score${hasHumanLabels ? " — human rating (1 = looks right, 2 = minor, 3 = broken) from `internal/research/human-labels.json`" : ""}:

${lowestHeader}
${lowestRows.join("\n")}

---

## Related

- **OSS comparison** — run \`npm run test:benchmark\` after a loop; see [BENCHMARK.md](./BENCHMARK.md)
- **Regenerate** — \`npm run test:suite\` (full) or \`npm run test:suite:priority\` (10-case subset), then \`npm run docs:sync\`
`;
}

