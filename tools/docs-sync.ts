async function fileExists(filePath: string): Promise<boolean> {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function readJson<T>(filePath: string): Promise<T | null> {
  if (!(await fileExists(filePath))) return null;
  return JSON.parse(await readFile(filePath, "utf-8")) as T;
}

function fmtPct(value: number | null | undefined): string {
  return value !== null && value !== undefined ? `${value.toFixed(2)}%` : "—";
}

function fmtScore(value: number | null | undefined): string {
  return value !== null && value !== undefined ? value.toFixed(2) : "—";
}

function fmtMs(value: number | null | undefined): string {
  return value !== null && value !== undefined ? `${value.toFixed(1)} ms` : "—";
}

function fmtDelta(value: number | null | undefined): string {
  if (value === null || value === undefined) return "—";
  return `${value >= 0 ? "+" : ""}${value.toFixed(2)}`;
}

/** Human-readable age, e.g. "2h 15m", "3d 4h". Used for staleness warnings only. */
function humanAge(ms: number): string {
  const abs = Math.abs(ms);
  const minutes = Math.floor(abs / 60_000);
  const hours = Math.floor(minutes / 60);
  const days = Math.floor(hours / 24);
  if (days > 0) return `${days}d ${hours % 24}h`;
  if (hours > 0) return `${hours}h ${minutes % 60}m`;
  return `${minutes}m`;
}

function rollup(cases: CaseResult[]): SuiteRollup {
  const completed = cases.filter((c) => !c.error);
  const avg = (values: Array<number | null | undefined>): number | null => {
    const nums = values.filter((v): v is number => v !== null && v !== undefined);
    return nums.length > 0 ? nums.reduce((s, v) => s + v, 0) / nums.length : null;
  };
  return {
    caseCount: cases.length,
    completedCount: completed.length,
    xmlPassCount: cases.filter((c) => c.xmlPassed).length,
    visual: avg(completed.map((c) => c.visualScore)),
    editability: avg(completed.map((c) => c.editability)),
    performance: avg(completed.map((c) => c.performanceScore)),
    engine: avg(completed.map((c) => c.engineScore)),
    avgPerformanceMs: avg(completed.map((c) => c.compileMs)),
    layout: avg(completed.map((c) => c.layoutFidelityScore)),
    pixelMatch: avg(completed.map((c) => c.matchPercent)),
  };
}

